
package J07035.util;

public class Subjects {
    private String id, name, count;

    public Subjects(String id, String name, String count) {
        this.id = id;
        this.name = name;
        this.count = count;
    }

    public String getId() {
        return id;
    }
    
    public String getName() {
        return name;
    }
}
